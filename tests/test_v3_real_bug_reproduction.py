def test_copy_test_overlay_copies_fix_test_and_rejects_escape(tmp_path):
    bug = tmp_path / "bug"
    fix = tmp_path / "fix"
    (bug / "tests").mkdir(parents=True)
    (fix / "tests").mkdir(parents=True)
    (bug / "tests" / "test_core.py").write_text("old", encoding="utf-8")
    (fix / "tests" / "test_core.py").write_text("new", encoding="utf-8")

    result = copy_test_overlay(bug, fix, ["tests/test_core.py", "../outside.py"])

    assert result["status"] == "fail"
    assert result["copied_count"] == 1
    assert result["errors"] == ["unsafe_test_overlay_path:../outside.py"]
    assert (bug / "tests" / "test_core.py").read_text(encoding="utf-8") == "new"


def test_prepare_case_uses_bug_and_fix_sha_and_overlays_tests(tmp_path):
    stale = tmp_path / "bug" / "repository_checkout" / "stale.txt"
    stale.parent.mkdir(parents=True)
    stale.write_text("contaminated", encoding="utf-8")
    calls = []

    def fake_checkout(**kwargs):
        calls.append(kwargs)
        root = Path(kwargs["output_dir"]) / "repository_checkout"
        (root / "tests").mkdir(parents=True)
        (root / "tests" / "test_core.py").write_text(
            "fixed-test" if "fix" in str(kwargs["output_dir"]) else "bug-test",
            encoding="utf-8",
        )
        return {
            "status": "pass",
            "reason": "fixture",
            "checkout_path": str(root),
            "ref": kwargs["ref"],
            "checkout_method": "fixture",
        }

    case = _case()
    preparation = prepare_real_bug_case(case, tmp_path, checkout=fake_checkout)

    assert preparation["status"] == "pass"
    assert [item["ref"] for item in calls] == ["a" * 40, "b" * 40]
    assert stale.exists() is False
    assert (
        Path(preparation["bug_root"]) / "tests" / "test_core.py"
    ).read_text(encoding="utf-8") == "fixed-test"


