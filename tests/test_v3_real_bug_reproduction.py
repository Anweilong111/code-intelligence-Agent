def test_execute_commands_injects_only_repository_pythonpath(tmp_path):
    docs_src = tmp_path / "docs_src"
    docs_src.mkdir()
    observed_env = {}

    def fake_runner(command, **kwargs):
        del command
        observed_env.update(kwargs["env"])
        return subprocess.CompletedProcess([], 0, stdout="1 passed", stderr="")

    result = execute_test_commands(
        [["{python}", "-m", "pytest", "-q", "tests"]],
        repository_root=tmp_path,
        python_executable=Path(__file__),
        timeout=5,
        runner=fake_runner,
        test_environment={"pythonpath_entries": ["docs_src"]},
    )

    assert result["status"] == "pass"
    assert observed_env["PYTHONPATH"] == str(docs_src.resolve())
    assert result["environment"]["pythonpath_entries"] == ["docs_src"]

    rejected = execute_test_commands(
        [["{python}", "-m", "pytest", "-q", "tests"]],
        repository_root=tmp_path,
        python_executable=Path(__file__),
        timeout=5,
        runner=fake_runner,
        test_environment={"pythonpath_entries": ["../outside"]},
    )
    assert rejected["status"] == "fail"
    assert rejected["reason"] == "unsafe_pythonpath_entry"

    optional = execute_test_commands(
        [["{python}", "-m", "pytest", "-q", "tests"]],
        repository_root=tmp_path,
        python_executable=Path(__file__),
        timeout=5,
        runner=fake_runner,
        test_environment={"optional_pythonpath_entries": ["not-created"]},
    )
    assert optional["status"] == "pass"
    assert optional["environment"]["optional_pythonpath_entries_missing"] == [
        "not-created"
    ]

    unsupported_tool = execute_test_commands(
        [["{python}", "-m", "pytest", "-q", "tests"]],
        repository_root=tmp_path,
        python_executable=Path(__file__),
        timeout=5,
        runner=fake_runner,
        test_environment={"required_tools": ["not-allowlisted"]},
    )
    assert unsupported_tool["status"] == "fail"
    assert unsupported_tool["reason"] == (
        "required_test_tool_missing:not-allowlisted"
    )


