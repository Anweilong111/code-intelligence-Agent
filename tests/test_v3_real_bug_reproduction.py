def test_reproduction_rejects_zero_test_regression(tmp_path):
    case = _case()
    preparation = {
        "status": "pass",
        "reason": "fixture",
        "bug_root": str(tmp_path / "bug"),
        "fix_root": str(tmp_path / "fix"),
        "bug_checkout": {"status": "pass", "ref": "a" * 40},
        "fix_checkout": {"status": "pass", "ref": "b" * 40},
        "test_overlay": {"status": "pass", "files": []},
    }
    (tmp_path / "bug").mkdir()
    (tmp_path / "fix").mkdir()
    calls = 0

    def fake_runner(command, **kwargs):
        nonlocal calls
        del kwargs
        if "-c" in command:
            return subprocess.CompletedProcess(command, 0, stdout="3.11.9\n", stderr="")
        calls += 1
        if calls == 1:
            return subprocess.CompletedProcess(command, 1, stdout="1 failed", stderr="")
        if calls == 2:
            return subprocess.CompletedProcess(command, 0, stdout="1 passed", stderr="")
        return subprocess.CompletedProcess(
            command,
            0,
            stdout="",
            stderr="Ran 0 tests in 0.000s\n\nOK\n",
        )

    result = reproduce_real_bug_case(
        case,
        preparation,
        python_executable=Path(__file__),
        runner=fake_runner,
    )

    assert result["status"] == "fail"
    assert result["acceptance"]["fix_full_regression_passed"] is False


