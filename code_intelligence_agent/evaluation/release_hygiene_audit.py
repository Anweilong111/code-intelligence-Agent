from __future__ import annotations

import argparse
import json
import os
import re
import subprocess
import sys
from pathlib import Path
from typing import Any


SECRET_PATTERN = re.compile(
    r"\bsk-[A-Za-z0-9._-]{16,}\b"
    r"|(?:CIA_LLM_API_KEY|DEEPSEEK_API_KEY|DASHSCOPE_API_KEY)\s*=\s*sk-",
    re.IGNORECASE,
)
BAD_BOUNDARY_PHRASES = [
    "LLM judge " + "可以替代 pytest sandbox",
    "LLM judge 是最终成功标准",
    "可以绕过 pytest sandbox",
]
TOOL_TRACE_PATTERNS = [
    re.compile(r"\bGenerated by (?:ChatGPT|Codex|Claude|Cursor)\b", re.IGNORECASE),
    re.compile(r"\bAI-generated\b", re.IGNORECASE),
    re.compile(r"\bChatGPT\b", re.IGNORECASE),
    re.compile(r"\bCodex\b", re.IGNORECASE),
]
IGNORED_OUTPUT_PATTERNS = [
    "outputs/",
    "outputs_v2/",
    "outputs_v3/",
    "outputs_demo/",
    "outputs_smoke/",
    "outputs_live/",
    "outputs_docx_render/",
    "htmlcov/",
    ".pytest_cache/",
    "__pycache__/",
]
IGNORED_FILE_SUFFIXES = [
    ".docx",
    ".pyc",
    ".pyo",
]
REQUIRED_GITIGNORE_PATTERNS = [
    "outputs/",
    "outputs_v2/",
    "outputs_v3/",
    "outputs_demo/",
    "outputs_smoke/",
    "outputs_live/",
    "htmlcov/",
    ".pytest_cache/",
    "*.docx",
    ".env",
    ".env.*",
]
TEXT_SUFFIXES = {
    "",
    ".cfg",
    ".ini",
    ".json",
    ".md",
    ".py",
    ".toml",
    ".txt",
    ".yaml",
    ".yml",
}


def build_release_hygiene_audit(
    root: str | Path,
    *,
    candidate_paths: list[str] | None = None,
) -> dict[str, Any]:
    root_path = Path(root).resolve()
    if candidate_paths is None:
        candidates, candidate_source = _discover_candidate_paths(root_path)
    else:
        candidates = sorted(
            {str(item).replace("\\", "/") for item in candidate_paths}
        )
        candidate_source = "explicit"
    checks = [
        _gitignore_check(root_path),
        _tracked_cache_check(candidates, candidate_source=candidate_source),
        _secret_check(root_path, candidates),
        _public_doc_boundary_check(root_path, candidates),
        _public_doc_tool_trace_check(root_path, candidates),
    ]
    failed = [check for check in checks if not bool(check.get("passed", False))]
    return {
        "status": "pass" if not failed else "fail",
        "reason": (
            "release_hygiene_clean"
            if not failed
            else "release_hygiene_issues_found"
        ),
        "candidate_file_count": len(candidates),
        "candidate_source": candidate_source,
        "check_count": len(checks),
        "passed_check_count": len(checks) - len(failed),
        "failed_check_count": len(failed),
        "failed_checks": [str(check.get("name") or "") for check in failed],
        "checks": checks,
    }


def render_release_hygiene_audit_markdown(audit: dict[str, Any]) -> str:
    lines = [
        "# Release Hygiene Audit",
        "",
        f"- Status: `{audit.get('status')}`",
        f"- Reason: `{audit.get('reason')}`",
        f"- Candidate Files: `{audit.get('candidate_file_count')}`",
        f"- Candidate Source: `{audit.get('candidate_source') or 'unknown'}`",
        f"- Checks: `{audit.get('passed_check_count')}/{audit.get('check_count')}` pass",
        "",
        "| Check | Status | Evidence |",
        "| --- | --- | --- |",
    ]
    for check_value in _list(audit.get("checks")):
        check = _dict(check_value)
        evidence = str(check.get("evidence") or "")
        offenders = [str(item) for item in _list(check.get("offenders"))]
        if offenders:
            evidence = f"{evidence}; offenders=" + ", ".join(offenders[:8])
        lines.append(
            "| "
            f"{_markdown_cell(check.get('name'))} | "
            f"{'pass' if check.get('passed') else 'fail'} | "
            f"{_markdown_cell(evidence)} |"
        )
    lines.append("")
    return "\n".join(lines)


def write_release_hygiene_audit_artifacts(
    audit: dict[str, Any],
    output_dir: str | Path,
) -> dict[str, str]:
    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)
    json_path = output_path / "release_hygiene_audit.json"
    markdown_path = output_path / "release_hygiene_audit.md"
    json_path.write_text(
        json.dumps(audit, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    markdown_path.write_text(
        render_release_hygiene_audit_markdown(audit),
        encoding="utf-8",
    )
    return {
        "release_hygiene_audit_json": str(json_path),
        "release_hygiene_audit_markdown": str(markdown_path),
    }


def _discover_candidate_paths(root: Path) -> tuple[list[str], str]:
    git_candidates = _candidate_paths_from_git(root)
    if git_candidates is not None:
        return git_candidates, "git"
    return _candidate_paths_from_filesystem(root), "filesystem_snapshot"


def _candidate_paths_from_git(root: Path) -> list[str] | None:
    top_level = subprocess.run(
        ["git", "rev-parse", "--show-toplevel"],
        cwd=str(root),
        capture_output=True,
        text=True,
        check=False,
    )
    if top_level.returncode != 0:
        return None
    try:
        discovered_root = Path(top_level.stdout.strip()).resolve()
    except (OSError, RuntimeError):
        return None
    if discovered_root != root:
        return None
    completed = subprocess.run(
        [
            "git",
            "ls-files",
            "--cached",
            "--others",
            "--exclude-standard",
        ],
        cwd=str(root),
        capture_output=True,
        text=True,
        check=False,
    )
    if completed.returncode != 0:
        return None
    return sorted(
        {
            line.strip().replace("\\", "/")
            for line in completed.stdout.splitlines()
            if line.strip()
        }
    )


def _candidate_paths_from_filesystem(root: Path) -> list[str]:
    candidates = []
    for current_root, directories, filenames in os.walk(root, followlinks=False):
        current = Path(current_root)
        directories[:] = [
            name
            for name in directories
            if name != ".git" and not (current / name).is_symlink()
        ]
        for filename in filenames:
            path = current / filename
            try:
                candidates.append(path.relative_to(root).as_posix())
            except ValueError:
                continue
    return sorted(set(candidates))


def _gitignore_check(root: Path) -> dict[str, Any]:
    gitignore = root / ".gitignore"
    text = gitignore.read_text(encoding="utf-8") if gitignore.exists() else ""
    missing = [item for item in REQUIRED_GITIGNORE_PATTERNS if item not in text]
    return _check(
        "gitignore_release_outputs_and_secrets",
        not missing,
        "required ignore patterns present",
        missing,
    )


def _tracked_cache_check(
    candidates: list[str],
    *,
    candidate_source: str,
) -> dict[str, Any]:
    offenders = [
        path
        for path in candidates
        if _is_ignored_output_path(path) or _has_ignored_suffix(path)
    ]
    return _check(
        "no_tracked_local_outputs_or_binary_docs",
        not offenders,
        f"{candidate_source} candidate set excludes local caches, outputs, coverage, and docx",
        offenders,
    )


def _secret_check(root: Path, candidates: list[str]) -> dict[str, Any]:
    offenders: list[str] = []
    for path in candidates:
        text = _read_text_candidate(root, path)
        if text is not None and SECRET_PATTERN.search(text):
            offenders.append(path)
    return _check(
        "no_raw_api_keys",
        not offenders,
        "no sk-style raw API keys in candidate text files",
        offenders,
    )


def _public_doc_boundary_check(root: Path, candidates: list[str]) -> dict[str, Any]:
    offenders: list[str] = []
    for path in candidates:
        if not _is_public_doc(path):
            continue
        text = _read_text_candidate(root, path)
        if text is None:
            continue
        if any(phrase in text for phrase in BAD_BOUNDARY_PHRASES):
            offenders.append(path)
    return _check(
        "public_docs_keep_sandbox_authority_boundary",
        not offenders,
        "LLM judge is not documented as pytest/sandbox replacement",
        offenders,
    )


def _public_doc_tool_trace_check(root: Path, candidates: list[str]) -> dict[str, Any]:
    offenders: list[str] = []
    for path in candidates:
        if not _is_public_doc(path):
            continue
        text = _read_text_candidate(root, path)
        if text is None:
            continue
        if any(pattern.search(text) for pattern in TOOL_TRACE_PATTERNS):
            offenders.append(path)
    return _check(
        "public_docs_have_no_tool_signature_traces",
        not offenders,
        "public docs avoid assistant/tool generation signatures",
        offenders,
    )


def _is_public_doc(path: str) -> bool:
    normalized = path.replace("\\", "/")
    if normalized in {
        "README.MD",
        "RESUME_AGENT_PROJECT.md",
        "INTERVIEW_QA_AGENT_PROJECT.md",
    }:
        return True
    return normalized.startswith("docs/") and normalized.endswith((".md", ".MD"))


def _read_text_candidate(root: Path, path: str) -> str | None:
    file_path = root / path
    if not file_path.exists() or not file_path.is_file():
        return None
    if file_path.suffix.lower() not in TEXT_SUFFIXES:
        return None
    try:
        return file_path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        return None


def _is_ignored_output_path(path: str) -> bool:
    normalized = path.replace("\\", "/")
    return any(
        normalized == item.rstrip("/") or normalized.startswith(item)
        for item in IGNORED_OUTPUT_PATTERNS
    )


def _has_ignored_suffix(path: str) -> bool:
    return any(path.lower().endswith(suffix) for suffix in IGNORED_FILE_SUFFIXES)


def _check(
    name: str,
    passed: bool,
    evidence: str,
    offenders: list[str],
) -> dict[str, Any]:
    return {
        "name": name,
        "passed": bool(passed),
        "evidence": evidence,
        "offender_count": len(offenders),
        "offenders": offenders[:50],
    }


def _dict(value: Any) -> dict[str, Any]:
    return value if isinstance(value, dict) else {}


def _list(value: Any) -> list[Any]:
    return value if isinstance(value, list) else []


def _markdown_cell(value: Any) -> str:
    return str(value or "").replace("|", "\\|").replace("\n", " ")


def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Audit release hygiene for GitHub-ready project packaging."
    )
    parser.add_argument("output_dir", help="Directory for audit artifacts.")
    parser.add_argument("--root", default=".", help="Repository root.")
    parser.add_argument("--format", choices=["json", "markdown"], default="markdown")
    parser.add_argument("--require-pass", action="store_true")
    return parser


def main(argv: list[str] | None = None) -> None:
    parser = build_arg_parser()
    args = parser.parse_args(argv)
    audit = build_release_hygiene_audit(args.root)
    write_release_hygiene_audit_artifacts(audit, args.output_dir)
    if args.format == "json":
        print(json.dumps(audit, indent=2, ensure_ascii=False))
    else:
        print(render_release_hygiene_audit_markdown(audit))
    if args.require_pass and audit["status"] != "pass":
        raise SystemExit(1)


if __name__ == "__main__":
    main(sys.argv[1:])
